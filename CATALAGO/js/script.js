/*funcion de disminuir la cantidad del producto*/
function validacantidad(form){
	var boton = document.getElementById("boton1");
	if (form.cantidad1.value<=0) {
		form.cantidad1.value=0;
		boton.disabled=true;
		alert("producto no disponible");
	}
	

	
};

